// switch statement
const grade = "C";

switch (grade) {
  case "A":
    console.log("Você tirou uma nota: A");
    break;
  case "B":
    console.log("Você tirou uma nota: B");
    break;
  case "C":
    console.log("Você tirou uma nota: C");
    break;
  case "D":
    console.log("Você tirou uma nota: D");
    break;
  case "E":
    console.log("Você tirou uma nota: E");
    break;
  default:
    console.log("nota inválida.");
}
